def normalize_language(language):
    if language in SUPPORTED_LANGUAGES:
        return language
    return DEFAULT_LANGUAGE


def translate_html(html, language):
    if language != "en":
        return html

    for source in sorted(EN_TRANSLATIONS, key=len, reverse=True):
        html = html.replace(source, EN_TRANSLATIONS[source])
    return html
